def draw_box(draw: ImageDraw.ImageDraw, xy, text: str, font, fill="#F5FBFF", outline="#28618A") -> None:
    draw.rounded_rectangle(xy, radius=12, fill=fill, outline=outline, width=3)
    x0, y0, x1, y1 = xy
    wrapped: list[str] = []
    for source_line in text.splitlines():
        if not source_line.strip():
            wrapped.append("")
            continue
        wrapped.extend(textwrap.wrap(source_line, width=max(14, int((x1 - x0) / 16))))
    total_h = len(wrapped) * 22
    y = y0 + (y1 - y0 - total_h) / 2
    for line in wrapped:
        bbox = draw.textbbox((0, 0), line, font=font)
        draw.text((x0 + (x1 - x0 - (bbox[2] - bbox[0])) / 2, y), line, fill="#102A43", font=font)
        y += 24


def arrow(draw: ImageDraw.ImageDraw, start, end, fill="#28618A") -> None:
    draw.line([start, end], fill=fill, width=4)
    x0, y0 = start
    x1, y1 = end
    if x1 >= x0:
        points = [(x1, y1), (x1 - 14, y1 - 8), (x1 - 14, y1 + 8)]
    else:
        points = [(x1, y1), (x1 + 14, y1 - 8), (x1 + 14, y1 + 8)]
    draw.polygon(points, fill=fill)


