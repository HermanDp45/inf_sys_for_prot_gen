def clean_pdf_text(text: str) -> str:
    text = re.sub(r"\s+", " ", text)
    text = text.replace("ё", "е")
    text = re.sub(r"^\s*\d+\s*", "", text)
    text = text.replace("2 Основная часть", "")
    text = text.replace("3 Заключение", "")
    text = re.sub(r"СПИСОК ЛИТЕРАТУРЫ.*", "", text, flags=re.I)
    return text.strip()


def split_sentences(text: str) -> list[str]:
    parts = re.split(r"(?<=[.!?])\s+(?=[А-ЯA-Z0-9])", text)
    paragraphs: list[str] = []
    current: list[str] = []
    for part in parts:
        current.append(part.strip())
        if sum(len(x) for x in current) > 620:
            paragraphs.append(" ".join(current))
            current = []
    if current:
        paragraphs.append(" ".join(current))
    return [p for p in paragraphs if len(p) > 80]


def extract_nir_research_text() -> list[str]:
    reader = PdfReader(str(NIR_PDF))
    # Physical pages 4..31 contain the NIR main part. Contents, old intro,
    # conclusion and bibliography are intentionally not reused verbatim.
    raw = "\n".join((page.extract_text() or "") for page in reader.pages[3:31])
    raw = clean_pdf_text(raw)
    raw = re.sub(r"\b2\.(\d+)", r"1.\1", raw)
    raw = re.sub(r"\b2\.(\d+)\.(\d+)", r"1.\1.\2", raw)
    return split_sentences(raw)


def extract_experiment_notes() -> list[tuple[str, list[str]]]:
    source = Document(str(EXPERIMENTS_DOCX))
    sections: list[tuple[str, list[str]]] = []
    current_title = ""
    current: list[str] = []
    for paragraph in source.paragraphs:
        text = paragraph.text.strip()
        if not text or text.startswith("Рисунок"):
            continue
        looks_like_title = (
            len(text) < 80
            and not text.endswith(".")
            and not text.startswith(("Модель:", "Цель", "Сравниваются", "Дизайн", "Интерпретация", "Итого"))
        )
        if looks_like_title:
            if current_title and current:
                sections.append((current_title, current))
            current_title = text
            current = []
        else:
            current.append(text)
    if current_title and current:
        sections.append((current_title, current))
    return sections


